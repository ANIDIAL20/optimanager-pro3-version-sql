/**
 * Firebase Admin Environment Setup Helper
 * 
 * This script helps you set up your .env.local file with the correct Firebase Admin credentials.
 * 
 * Usage:
 * 1. Download your Firebase service account JSON from:
 *    Firebase Console → Project Settings → Service Accounts → Generate Private Key
 * 2. Save the JSON file and run: node setup-firebase-env.js path/to/serviceAccountKey.json
 * 3. This will create/update your .env.local file with properly formatted credentials
 */

const fs = require('fs');
const path = require('path');

// Get the JSON file path from command line arguments
const jsonFilePath = process.argv[2];

if (!jsonFilePath) {
    console.log('❌ Error: Please provide the path to your Firebase service account JSON file.');
    console.log('\nUsage: node setup-firebase-env.js path/to/serviceAccountKey.json\n');
    console.log('To get your service account key:');
    console.log('1. Go to https://console.firebase.google.com/');
    console.log('2. Select your project');
    console.log('3. Go to Project Settings → Service Accounts');
    console.log('4. Click "Generate new private key"');
    console.log('5. Save the downloaded JSON file');
    console.log('6. Run this script with the path to that file\n');
    process.exit(1);
}

// Check if file exists
if (!fs.existsSync(jsonFilePath)) {
    console.log(`❌ Error: File not found: ${jsonFilePath}`);
    process.exit(1);
}

try {
    // Read and parse the JSON file
    const serviceAccount = JSON.parse(fs.readFileSync(jsonFilePath, 'utf8'));
    
    // Validate required fields
    if (!serviceAccount.project_id || !serviceAccount.client_email || !serviceAccount.private_key) {
        console.log('❌ Error: Invalid service account JSON. Missing required fields.');
        process.exit(1);
    }
    
    // Prepare the .env.local content
    const envContent = `# Firebase Admin SDK Configuration
# Auto-generated by setup-firebase-env.js on ${new Date().toISOString()}

FIREBASE_PROJECT_ID=${serviceAccount.project_id}
FIREBASE_CLIENT_EMAIL=${serviceAccount.client_email}
FIREBASE_PRIVATE_KEY="${serviceAccount.private_key}"

# Note: This file contains sensitive credentials and should NEVER be committed to version control.
# It is already in .gitignore by default.
`;
    
    // Write to .env.local
    const envPath = path.join(__dirname, '.env.local');
    fs.writeFileSync(envPath, envContent, 'utf8');
    
    console.log('✅ Success! Your .env.local file has been created/updated.');
    console.log('\nCredentials configured:');
    console.log(`   Project ID: ${serviceAccount.project_id}`);
    console.log(`   Client Email: ${serviceAccount.client_email}`);
    console.log(`   Private Key: ✓ (${serviceAccount.private_key.length} characters)`);
    console.log('\n🚀 You can now restart your development server with: npm run dev');
    console.log('\n⚠️  Important: Keep your service account JSON file secure and do not commit it to git!');
    
} catch (error) {
    console.log('❌ Error reading or parsing the JSON file:', error.message);
    process.exit(1);
}
